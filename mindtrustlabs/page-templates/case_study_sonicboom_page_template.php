									<p>What is responsive design, anyway? Go on any website on your computer and re-size the screen of the browser.</p>
									<p>If you can see the images, fonts, and more adjusting to the size of the browser, the website is "responsive."</p>
									<p>One Code Base: Managing a responsive website is easy because you don't have multiple code-bases, like you would if you had a traditional mobile site companion to go along with a desktop site</p>
								</div>
								<div class="col-sm-5 col-sm-push-2">
									<p><strong>One Domain:</strong> Responsive websites are on one domain (instead of multiple, like mobile websites), which means</p>
									<p><strong>Searce Engine Friendly:</strong> Search engines (primarily Google) have indicated that they may penalize websites that aren't responsive in the future.</p>
									<p><strong>User Experience-Friendly:</strong> With a rapidly growing user base browsing websites on mobile devices, catering to the mobile user is becoming increasingly more important. For websites that require conversions - filling out contact forms and making purchases, for example - the user experience is paramount. Responsive websites make the user experience seamless across all devices so companies don't have to worry about screen sizes.</p>
